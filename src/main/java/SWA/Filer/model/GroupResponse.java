package SWA.Filer.model;

public class GroupResponse {
    private int id;
    private String name;

    public GroupResponse() {
    }

    public GroupResponse(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
